package entity.spring;

public class Pencil extends Product {

    private String color;

    private boolean rubber;

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public String isRubber() {
        return color;
    }

    public void setRubber(boolean rubber) {
        this.rubber = rubber;
    }
}
